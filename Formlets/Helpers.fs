namespace Formlets

[<AutoOpen>]
module Helpers = 
    /// Builds a pair (2-tuple)
    let t2 a b = a,b

    /// Builds a triple (3-tuple)
    let t3 a b c = a,b,c

    /// Builds a 4-tuple
    let t4 a b c d = a,b,c,d

    /// Builds a 5-tuple
    let t5 a b c d e = a,b,c,d,e

    /// Builds a 6-tuple
    let t6 a b c d e f = a,b,c,d,e,f

    /// Builds a 7-tuple
    let t7 a b c d e f g = a,b,c,d,e,f,g

    /// Builds a 8-tuple
    let t8 a b c d e f g h = a,b,c,d,e,f,g,h


